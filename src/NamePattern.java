// Develop a program to display your name in the below pattern.
public class NamePattern {

	public static void main(String[] args) {
		System.out.println("H");
		System.out.println("\tA");
		System.out.println("\t\tR");
		System.out.println("\t\t\tI");
		
		System.out.println("\t Hari Krishana");
		System.out.println("Ha\tri Krishana");
		System.out.println("Hari kri\tshna");
		
		
	}

}
