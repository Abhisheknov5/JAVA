package p1;
public class F {
	//I declare all the variable as a static.
	private static int a=10;
	        static int b=20;
	protected static int c=30;
	public static int d=40;
	
	public static void main(String[] args) {
		/*
		 When we declare as static do I need Object Reference 
		 Not Required.
		 We can directly use class name within the class we can 
		 access directly by their name. 
		 */
		System.out.println("a:"+a);
		System.out.println("b:"+b);
		System.out.println("c:"+c);
		System.out.println("d:"+d);
		
		
	}

}

